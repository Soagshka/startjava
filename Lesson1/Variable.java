public class Variable {
	public static void main(String[] args) {
		byte proccessorNumber = 5;
		short bit = 64;
		int memory = 8;
		long processorType = 8650;
		float cpuFrequency = 2.11f;
		double installedMemory = 31.9;
		char operationSystem = 'W';
		boolean isStarted = true;

		System.out.println(" proccessorNumber === " + proccessorNumber);
		System.out.println(" bit === " + bit);
		System.out.println(" memory === " + memory);
		System.out.println(" processorType === " + processorType);
		System.out.println(" cpuFrequency === " + cpuFrequency);
		System.out.println(" installedMemory === " + installedMemory);
		System.out.println(" operationSystem === " + operationSystem);
		System.out.println(" isStarted === " + isStarted);
	}
}